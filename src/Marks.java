public class Marks {

    private String name;
    private String sex;
    private int marks;

    public Marks(String name, String sex, int marks) {
        this.name = name;
        this.sex = sex;
        this.marks = marks;
    }

    public String getName() {
        return name;
    }

    public String getSex() {
        return sex;
    }

    public int getMarks() {
        return marks;
    }
}
